export function normalizeLegacyPath(path: string, packFormat: number): string {
  // pack_format <= 3 corresponds to 1.12.2 and older
  if (packFormat > 3) return path;

  let normalized = path;

  // 1. Fix structural changes (blocks -> block, items -> item)
  if (normalized.startsWith('assets/minecraft/textures/blocks/')) {
    normalized = normalized.replace('assets/minecraft/textures/blocks/', 'assets/minecraft/textures/block/');
  } else if (normalized.startsWith('assets/minecraft/textures/items/')) {
    normalized = normalized.replace('assets/minecraft/textures/items/', 'assets/minecraft/textures/item/');
  }

  // 2. Fix filenames
  const match = normalized.match(/^(assets\/minecraft\/textures\/(?:block|item)\/)(.*?)(\.png|\.png\.mcmeta)?$/);
  if (match) {
    const prefix = match[1];
    const name = match[2];
    const suffix = match[3] || '';

    if (prefix.includes('/block/')) {
      if (blockRenames[name]) {
        return `${prefix}${blockRenames[name]}${suffix}`;
      }
    } else if (prefix.includes('/item/')) {
      if (itemRenames[name]) {
        return `${prefix}${itemRenames[name]}${suffix}`;
      }
    }
  }

  return normalized;
}
